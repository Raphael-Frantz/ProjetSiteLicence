<section class="mysection bg-light" id="procedure">
  <div class="container">
    <h2 class="mb-4">Procédure à suivre</h2>
    <p class="lead mb-1">
      Quelle que soit votre année, vous devez suivre les étapes suivantes :
    </p>
    <ol class="lead mb-0">
      <li> Contacter le responsable du diplôme en précisant votre année, votre entreprise et votre sujet de stage. </li>
      <li> Si le sujet est validé, vous recevez un formulaire Word à remplir. </li>
      <li>
        Le formulaire est à envoyer au responsable ET au secrétariat :
        <a href="mailto:cyril.rabat@univ-reims.fr">cyril.rabat@univ-reims.fr</a> ET <a href="mailto:jerome.muckli@univ-reims.fr">jerome.muckli@univ-reims.fr</a>
      </li>
      <li> Le formulaire permet d'éditer une convention qui doit être validée par la composante (UFR SEN). </li>
      <li> La convention doit être signée par l'étudiant, le responsable du diplôme, l'entreprise puis le directeur de la composante. </li>
      <li> Une fois la convention signée par toutes les parties, l'étudiant est autorisé à débuter son stage. </li>
      <li> 
        Un rapport doit être remis et une soutenance de stage est prévue (dates communiquées ultérieurement, modalités suivant l'année).
      </li>
    </ol>
  </div>
