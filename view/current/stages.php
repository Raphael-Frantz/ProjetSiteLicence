<section class="mysection" id="L1">
  <div class="container">
    <h2 class="mb-4">Stages en Licence 1</h2>
    <h3>Durée/période</h3>
    <p class="lead mb-1">
      Le stage de Licence 1 (EC PPRO0203) est d'au moins 2 semaines à temps-plein, ou une durée équivalente à temps partiel.
      Il doit impérativement se dérouler entre le début du S1 et la fin du S2.
      Il est possible qu'il se déroule du 27 mai 2019 au 17 juin 2019, la validation de l'année ne serait alors possible qu'en deuxième session.
      Il ne pourra dépasser le 31 août, date de la fin de l'année scolaire.
    </p>
    <h3>Sujet du stage</h3>
    <p class="lead mb-1">
      Le but du stage est de faire découvrir aux étudiants le monde professionnel et de leur faire retranscrire leur expérience.
      Le sujet du stage doit être validé par le responsable de la formation ; 
      la convention de stage, entre l’étudiant et l’entreprise, doit être signée au plus tard avant le début du stage.
    </p>
    <h3>Évaluations</h3>
    <p class="lead mb-1">
      Le stage sera évalué sur la base d’un rapport et d’une présentation orale.
      Le rapport devra contenir trois éléments différents : (1) présentation de l'entreprise et au moins deux métiers au sein de cette entreprise,
      (2) démarche que l'étudiant a mis en oeuvre pour la recherche du stage (CV, lettre(s) de motivation) et
      (3) présentation du travail effectué au quotidien.
      Le rapport devra être rédigé et la notation tiendra compte de l'orthographe et de la grammaire.
      La présentation orale sera de 10 minutes suivie de 5 minutes de questions.
    </p>
    <h3>Procédure à suivre</h3>
    <p class="lead mb-1">
      Consultez cette section : 
